require('dotenv').config();
const express = require('express');
const cors = require('cors');
const bcrypt = require('bcryptjs');
const jwt = require('jsonwebtoken');

const app = express();
app.use(express.json());
app.use(cors());

// In-memory 'database'
const users = [];
const orders = [];

// Helper to simulate DB ID generation
const generateId = () => Math.random().toString(36).substr(2, 9);

const authenticate = (req, res, next) => {
    const token = req.headers['authorization'];
    if (!token) return res.status(401).json({ error: 'Access denied' });

    try {
        const decoded = jwt.verify(token.split(" ")[1], process.env.JWT_SECRET || 'supersecretkey123');
        req.user = decoded;
        next();
    } catch (err) {
        res.status(400).json({ error: 'Invalid token' });
    }
};

const authorizeAdmin = (req, res, next) => {
    if (req.user.role !== 'admin') return res.status(403).json({ error: 'Access forbidden: Admins only' });
    next();
};

app.post('/api/auth/signup', async (req, res) => {
    try {
        const { name, email, password, role } = req.body;

        // Check if user exists
        if (users.find(u => u.email === email)) {
            return res.status(400).json({ error: 'User already exists' });
        }

        const hashedPassword = await bcrypt.hash(password, 10);
        const newUser = {
            _id: generateId(),
            name,
            email,
            password: hashedPassword,
            role: role || 'user'
        };

        users.push(newUser);
        res.status(201).json({ message: 'User registered successfully' });
    } catch (err) {
        res.status(400).json({ error: 'Error registering user', details: err.message });
    }
});

app.post('/api/auth/login', async (req, res) => {
    try {
        const { email, password } = req.body;
        const user = users.find(u => u.email === email);
        if (!user) return res.status(400).json({ error: 'User not found' });

        const isMatch = await bcrypt.compare(password, user.password);
        if (!isMatch) return res.status(400).json({ error: 'Invalid credentials' });

        const token = jwt.sign(
            { id: user._id, role: user.role },
            process.env.JWT_SECRET || 'supersecretkey123',
            { expiresIn: '1h' }
        );
        res.json({ token, user: { id: user._id, name: user.name, email: user.email, role: user.role } });
    } catch (err) {
        res.status(500).json({ error: 'Login error' });
    }
});

app.post('/api/orders', authenticate, async (req, res) => {
    try {
        const { items, totalAmount } = req.body;
        const newOrder = {
            _id: generateId(),
            userId: req.user.id,
            items,
            totalAmount,
            status: 'Pending',
            paymentStatus: 'Paid (Demo)',
            createdAt: new Date()
        };
        orders.push(newOrder);
        res.status(201).json({ message: 'Order placed successfully', order: newOrder });
    } catch (err) {
        res.status(500).json({ error: 'Error placing order' });
    }
});

app.get('/api/orders/my-orders', authenticate, async (req, res) => {
    try {
        const myOrders = orders
            .filter(o => o.userId === req.user.id)
            .sort((a, b) => b.createdAt - a.createdAt);
        res.json(myOrders);
    } catch (err) {
        res.status(500).json({ error: 'Error fetching orders' });
    }
});

app.get('/api/orders/all', authenticate, authorizeAdmin, async (req, res) => {
    try {
        const allOrders = orders.map(order => {
            const user = users.find(u => u._id === order.userId);
            return {
                ...order,
                userId: user ? { _id: user._id, name: user.name, email: user.email } : null
            };
        }).sort((a, b) => b.createdAt - a.createdAt);

        res.json(allOrders);
    } catch (err) {
        res.status(500).json({ error: 'Error fetching all orders' });
    }
});

app.put('/api/orders/:id/status', authenticate, authorizeAdmin, async (req, res) => {
    try {
        const { status } = req.body;
        const orderIndex = orders.findIndex(o => o._id === req.params.id);

        if (orderIndex === -1) return res.status(404).json({ error: 'Order not found' });

        orders[orderIndex].status = status;
        res.json(orders[orderIndex]);
    } catch (err) {
        res.status(500).json({ error: 'Error updating status' });
    }
});

const PORT = process.env.PORT || 5000;
app.listen(PORT, () => console.log(`Server running on port ${PORT}`));
